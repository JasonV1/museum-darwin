Charles Robert Darwin (Shrewsbury (Shropshire), 12 februari 1809 – Downe (Kent), 19 april 1882) was een Engels autodidact
op het gebied van natuurlijke historie, biologie en geologie.
Darwin ontleent zijn roem aan zijn theorie dat evolutie van soorten wordt gedreven door natuurlijke selectie.
Het bestaan van evolutie werd nog tijdens zijn leven binnen een groot deel van de wetenschappelijke gemeenschap geaccepteerd.
De acceptatie van natuurlijke selectie als aandrijvend mechanisme liet langer op zich wachten en is tegenwoordig onomstreden.

Darwin werd gelovig opgevoed en opgeleid volgens de filosofie van de in die tijd in Engeland gangbare natuurlijke theologie,
die religie en wetenschap verenigde. Dankzij de waarnemingen en ontdekkingen die hij in de loop van zijn leven deed ging hij
echter steeds meer twijfelen over zowel de gangbare ideeën over soortvorming als zijn persoonlijke geloof. Tijdens een onderzoeksreis
met het schip de Beagle (1831-1836) bezocht Darwin Zuid-Amerika, Australië, het zuiden van Afrika en diverse eilandengroepen
in de Grote en Indische Oceaan. Op al deze plekken bestudeerde hij de plaatselijke dieren, planten, fossielen en geologie.
Een groot deel van zijn verdere leven was gewijd aan het onderzoeken en classificeren van de op zijn reis verzamelde voorwerpen
en het was onder andere dankzij dit onderzoek dat hij op zijn theorie over het ontstaan van soorten kwam.

Darwins werk zorgde voor een revolutie binnen de wetenschap, maar had ook invloed op maatschappij, filosofie en religie.
De acceptatie van evolutie zette de mens neer als een diersoort, onderdeel van de natuur,
in plaats van een boven de natuur staande levensvorm.
Samen met de erfelijkheidsleer van Mendel vormt Darwins evolutietheorie tegenwoordig de basis van alle biologische kennis.
