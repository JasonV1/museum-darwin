    <h1>De evolutietheorie</h1>
    De evolutietheorie is de natuurwetenschappelijke verklaring voor de evolutie van het leven en voor de
    verscheidenheid aan soorten op Aarde.
    Ze beschrijft het proces waarbij erfelijke eigenschappen binnen een populatie van organismen veranderen in de loop
    van de generaties als
    gevolg van genetische variatie, voortplanting en natuurlijke selectie.

    Charles Darwin (1809-1882) wordt samen met Alfred Russel Wallace (1823-1913) beschouwd als de belangrijkste
    grondlegger van de
    evolutietheorie. Darwin zette zijn verklaring van het ontstaan van soorten door natuurlijke teeltkeus uiteen in zijn
    boek
    On the origin of species by means of natural selection, or the preservation of favoured races in the struggle for
    life.
    Al vrij kort nadat dit boek in 1859 verscheen, werd de evolutietheorie binnen de wetenschappelijke wereld algemeen
    aanvaard als
    verklaring voor het ontstaan van soorten, inclusief de mens. Gedurende anderhalve eeuw sinds Darwin heeft de
    evolutietheorie
    belangrijke ontwikkelingen doorgemaakt, met name door nieuwe inzichten op het gebied van de (moleculaire) genetica.
    Zo werd in de moderne synthese de evolutietheorie gecombineerd met de wetten van Mendel. Ook werd door het onderzoek
    naar genen en
    DNA aan de evolutietheorie een basis gegeven in de moleculaire biologie.

