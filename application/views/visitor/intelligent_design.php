    <h1>Intelligent Design</h1>

    Intelligent design (Engels, afgekort als ID, wordt vertaald als intelligent ontwerp) is de opvatting dat bepaalde
    kenmerken
    van het heelal en organismen het best worden verklaard als het werk van een intelligente "ontwerper".
    ID staat zodoende in contrast met de evolutietheorie, die erfelijkheid en selectiemechanismen (waaronder natuurlijke
    selectie)
    beschouwt als een afdoende verklaring voor het ontstaan van alle organismen.
    Hoewel er nog veel vragen open staan over het ontstaan van het leven en de erop volgende biologische evolutie,
    is er een overduidelijke consensus binnen de wetenschappelijke gemeenschap dat intelligent design geen wetenschap
    is,
    maar pseudowetenschap die gebruikmaakt van teleologische argumentatie.

    De naam intelligent design wordt metonymisch gebruikt voor zowel de opvatting als voor de beweging die zich hard
    maakt voor het opnemen
    van bovenstaande opvatting in het curriculum van scholen in de Verenigde Staten. Ook in andere landen zijn
    ID-aanhangers.

