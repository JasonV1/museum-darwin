    <h1>De geologische tijdperken</h1>
    Tijdperken worden ingedeeld in kleinere (sub-)tijdperken. De grootste tijdperken zijn de eonen, die worden opgedeeld
    in era's. De geschiedenis van de Aarde wordt ingedeeld in drie officiële eonen: Archeïcum, Proterozoïcum en
    Fanerozoïcum. Het Fanerozoïcum wordt weer ingedeeld in drie era's: Paleozoïcum, Mesozoïcum en Cenozoïcum. Op hun
    beurt worden era's weer ingedeeld in periodes. Voorbeelden van periodes uit het Paleozoïcum zijn het Carboon en het
    Perm, het Mesozoïcum bestaat uit het Trias, het Jura en het Krijt en het Cenozoïcum uit het Paleogeen, het Neogeen
    en het Kwartair. Deze periodes worden weer ingedeeld in tijdvakken, die weer ingedeeld worden in tijdsnedes. Zo
    bestaat het Neogeen uit de tijdvakken Mioceen en Plioceen. Soms worden tijdsnedes weer ingedeeld in chrons, de
    kleinste eenheden in de geologische tijdschaal.

    <h2>De Zaal</h2>
    Hier vind u informatie over de verschillende geologische perioden zoals het Cambrium.
