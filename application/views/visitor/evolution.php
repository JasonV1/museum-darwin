    <h1>Evolutie van de mens</h1>
    De evolutie van de mens, het proces waardoor de moderne mens is ontstaan uit eerder levende primaten, is onderwerp
    van studie van veel verschillende wetenschappelijk disciplines. Biologische antropologie, antropologie en genetica
    zijn hierbij de belangrijkste. Het begrip mens, in de context van de menselijke evolutie, verwijst naar het geslacht
    Homo. De studies over de menselijke evolutie gaan meestal ook over andere hominiden, zoals de Australopithecus.

