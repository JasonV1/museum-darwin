<h1>Galapagosreuzenschildpad</h1>

Deze soort komt alleen voor op de Galapagoseilanden en leeft in allerlei biotopen:
van met bomen of met cactussen begroeide plaatsen tot meer open landschappen.
Het is de enige soort reuzenschildpad die er leeft, hoewel er 10 in het wild levende ondersoorten zijn die iets verschillen.
De schildpad is overdag op het land te vinden, al etend en zonnebadend, maar zoekt 's nachts een modderpoel op waar hij in overnacht.
In de modder blijft de schildpad warm, waarschijnlijk speelt dit ook voor de digestie een rol.
Ook overdag wordt wel eens in de modder gebaad, waarschijnlijk om van parasieten af te komen.

