    <h1>Darwin zijn leven en reis</h1>

    Darwin werd geboren als vijfde kind en de jongste van twee zonen van de rijke arts Robert Waring Darwin (1766-1848)
    en diens vrouw Susannah Wedgwood (1765–1817). Zijn grootvader aan vaders zijde was de arts, dichter en filosoof
    Erasmus Darwin (1731-1802); zijn grootvader aan moeders zijde was de industrieel Josiah Wedgwood (1730-1795). Hoewel
    zijn beide ouders gelovig waren, was vader Robert Darwin een vrijdenker, die zijn kinderen meer vanwege conventies
    dan vanwege overtuiging liet dopen.
    Zijn vrouw nam de kinderen echter mee naar de mis in de Unitaristische kerk. Vanaf 1817 bezocht Charles de
    dorpsschool die door
    de priester van deze kerk geleid werd. Hetzelfde jaar, toen Charles acht jaar oud was, stierf zijn moeder.
    Vanaf september 1818 werd hij naar de Anglicaanse kostschool Shrewsbury School gestuurd, waar hij kost en onderdak
    vond.
    Hij toonde al jong een grote belangstelling voor de natuur. Hij verzamelde "alle mogelijke dingen, schelpen, zegels,
    munten en mineralen".
    Vooral vogels en insecten hadden zijn belangstelling. Geboeid las hij het verslag over de reis van de Duitser
    Alexander
    von Humboldt in Zuid-Amerika en “Een natuurlijke historie van Selborne” door dominee Gilbert White.

