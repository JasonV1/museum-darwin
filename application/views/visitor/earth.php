<h1>De Aarde</h1>

<h2>Geologische perioden</h2>

<img class="geological_periods" src="http://upload.wikimedia.org/wikipedia/commons/7/79/Geological_time_spiral.png" /> <br />

Al in de 18e eeuw werd de stratigrafische colom door geologen in verschillende eenheden (gesteentelagen) opgedeeld.
Dat deze eenheden voor verschillende tijdperken in de geschiedenis van de Aarde stonden, was ook toen al duidelijk.
Op basis van overeenkomsten konden gesteentelagen die in verschillende delen van de wereld aan het oppervlak liggen gecorreleerd worden.
Er blijven echter vooral op kleine schaal veel onduidelijkheden,
die met het verbeteren van de stratigrafische technieken en methodes steeds beter opgelost kunnen worden.<br />

